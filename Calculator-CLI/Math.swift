import Foundation

struct Math {
    
    func addieren(_ x: Int,_ y: Int) -> Int {
        return x + y
    }
    
    func subtrahieren(_ x: Int,_ y: Int) -> Int {
        return x - y
    }
    
    func multiplizieeren(_ x: Int,_ y: Int) -> Int {
        return x * y
    }
    
    func dividieren(_ x: Int,_ y: Int) -> Int {
        return x / y
    }
}
